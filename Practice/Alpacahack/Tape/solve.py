import string

enc1 = [
    0x08, 0x23, 0x03, 0x03, 0x13, 0x03, 0x13, 0x03, 0x01, 0x23, 
    0x31, 0x13, 0x11, 0xC8, 0x03, 0xC8, 0x03, 0x13, 0x01, 0xC8, 
    0x13, 0x13, 0x03, 0x13, 0x13, 0x11, 0x13, 0x23
]
enc2 = [
    0x02, 0x40, 0x80, 0x08, 0x08, 0x08, 0xC8, 0xC8, 0x80, 0x88, 
    0x08, 0x80, 0x88, 0x32, 0x08, 0x32, 0x80, 0x80, 0x80, 0x32, 
    0x08, 0x80, 0x08, 0x08, 0x48, 0x88, 0x80, 0xC8
]

assert len(enc1) == len(enc2)

charset = string.printable
size = len(enc1)
key = ""

for i in range(size):
    for j in charset:
        value = ord(j)
        value = (value >> 5) | (8 * value)
        s1, s2 = 0, 0
        if (value & 1 == 0):
            s1 = value & 0xCC
            s2 = value & 0x33
        else:
            s1 = value & 0x33
            s2 = value & 0xCC
        
        if (s1 == enc1[i]) and (s2 == enc2[i]):
            key += j
            break
    
print(key)